/* Operadores  de Incremento e Decremento */
let b = 0;

//Incremento
b++; // b = 1
b++; // b = 2
b++; // b = 3
b++; // b = 4
b++; // b = 5

//Decremento
b--; // b = 4
b--; // b = 3
b--; // b = 2
b--; // b = 1
b--; // b = 0

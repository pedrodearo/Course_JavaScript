/* Operadores Aritiméticos */
10 + 10;

10 - 10;

10 * 10;

10 / 10;

10 % 10;

/* Operadores de Agrupamento */
10 + 10 - (5 * 2) / 2;

/* Operadores de Atribuição */
let a = 10;

a += 5; // a = 15

a -= 5; // a = 10

a *= 5; // a = 50

a /= 5; // a = 10

a %= 5; // a = 0

/* Operadores de Encadeamento */

/* Operadores Lógicos "OU (OR)" */

true || true; // True
true || false; // True
false || true; // True

false && false; // Fasle

//Exemplos
10 > 5 || 10 === "10"; // True

10 < 5 || 10 != 10; // False

/* DICA */

// O "True" prevalece o "False", logo, quando possiur um valor "True", o resuldade também será "True"

/* Comparação de dados irão retornar um resultado booleano (True or False) */

/* Operadores de Igualdade de "VALORES" */
10 == 10; // True
10 == 5; // False

/* Operadores de Igualdade de "VALORES" e "TIPAGEM" */
10 === 10; // True
10 === 5; // False
"10" === "10"; // True
"10" === "5"; // False
10 === "10"; // False

/* Operadores de Desigualdade de "VALORES" */
10 != 10; // False
10 != 5; // True

/* Operadores de Desigualdade de "VALORES" e "TIPAGEM" */
10 !== 10; // False
10 !== 5; // True
"10" !== "10"; // False
"10" !== "5"; // True
10 !== "10"; // True

/* Operadores  Relacionais*/
10 > 5; // True
10 >= 10; // true

10 < 5; // False
10 <= 5; // False
